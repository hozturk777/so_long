#include "libft.h"

int	ft_atoi(const char *str)
{
	int				i;
	int				sign;
	unsigned long	result;

	i = 0;
	sign = 1;
	result = 0;
	while (*(str + i) == 32 || (*(str + i) >= 9 && *(str + i) <= 13))
		i++;
	if (*(str + i) == '-' || *(str + i) == '+')
	{
		if (*(str + i) == '-')
			sign *= -1;
		i++;
	}
	while (*(str + i) != '\0' && ft_isdigit(*(str + i)))
	{
		result *= 10;
		result += *(str + i) - 48;
		i++;
	}
	return (result * sign);
}
